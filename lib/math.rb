def addition(num1, num2)
  sum = num1 + num2
  sum
end

def subtraction(num1, num2)
  sub = num1 - num2
  sub
end

def division(num1, num2)
  division = num1 / num2
  division
end

def multiplication(num1, num2)
  mult = num1 * num2
  mult
end

def modulo(num1, num2)
  mod = num1 % num2
end

def square_root(num)
  Math.sqrt(num)
end
